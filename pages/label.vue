    <h3 id="apports-et-engagements-li-s-au-label">
      Apports et engagements liés au label
    </h3>
    <h4>Que m’apporte le label, en tant que porteur de dispositif ?</h4>
    <ul>
      <li>
        L’identification publique de votre dispositif comme un dispositif de
        référence numérique au service de l’inclusion et de la médiation
        numérique.
      </li>
      <li>Une visibilité du dispositif labellisé :</li>
      <li>
        à travers sa mise en valeur sur La Base (badge « label NEC » affiché sur
        la page de présentation du dispositif),
      </li>
      <li>
        et à travers les actions de communication de l’ANCT sur les dispositifs
        labellisés ou en cours de labellisation lors des événements NEC locaux
        et NEC national comme en-dehors de ceux-ci : production de vidéos,
        interviews, newsletter, etc.
      </li>
      <li>
        L’accès à un réseau étendu d’acteurs de la médiation et de l’inclusion
        numérique (acteurs labellisés NEC, membres des réseaux NEC locaux et du
        réseau national).
      </li>
    </ul>
    <h4>À quoi engage l’obtention du label ?</h4>
    <p>
      Chaque porteur de dispositif labellisé ou en cours de labellisations
      s’engage à :
    </p>
    <ol>
      <li>
        Renseigner et mettre à jour les informations sur le dispositif dans la
        bibliothèque de la médiation et de l’inclusion numérique.
      </li>
      <li>
        Communiquer autour du label. Faire mention du label “Numérique en
        Commun[s]” sur les outils de communication associés ainsi que faire
        figurer le logo ANCT et celui du dispositif “Numérique en Commun[s]é sur
        les outils de communication promouvant le dispositif, dans le respect de
        la politique de la marque ANCT.
      </li>
    </ol>
