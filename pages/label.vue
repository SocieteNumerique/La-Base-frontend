        <em>Mobiliers, jeux, kits pédagogiques, etc.</em>
      </li>
      <li>
        <strong>Dispositifs méthodologiques et pédagogiques</strong>
        <em
          >Mise en réseau, exprimentation sur un public ou une thématique
          spécifique</em
        >
      </li>
    </ul>
