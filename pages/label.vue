    <h1 id="label-num-rique-en-commun-s-">Label Numérique en Commun[s]</h1>
    <h3 id="le-label-nec">Le Label NEC</h3>
    <p>
      Le label s’inscrit dans la continuité des actions portées collectivement
      par la communauté « Numérique en Commun[s] ».
    </p>
    <p>
      Conçu par l’ANCT, il vise à identifier, reconnaître et promouvoir les
      dispositifs concourant aux objectifs d’inclusion numérique sur l’ensemble
      du territoire.
    </p>
    <p>Le label poursuit trois objectifs :</p>
    <ol>
      <li>
        Permettre à tous de mobiliser les outils nécessaires à la montée en
        compétence numérique de la population (en facilitant l’identification de
        dispositifs de qualité pour renforcer la confiance dans les offres et
        les outils de médiation numérique)
      </li>
      <li>
        Soutenir la montée en gamme de la filière de la médiation numérique, en
        facilitant l’émergence et/ou la pérennisation de dispositifs de
        référence, répondant à des exigences de qualité (en favorisant les
        nouvelles collaborations entre acteurs de l’inclusion numérique, dans
        une logique d’émergence de communs numérique).
      </li>
      <li>
        Contribuer à la diffusion d’une culture numérique critique indispensable
        pour que les citoyens puissent se saisir des opportunités du numérique
        dans leur vie quotidienne (à travers une approche pédagogique, qui
        encourage l’accès de tous à un numérique ouvert, inclusif, durable,
        éthique et respectueux des droits et liberté des citoyens).
      </li>
    </ol>
